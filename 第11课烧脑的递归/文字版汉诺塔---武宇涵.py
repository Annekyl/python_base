#文字版汉诺塔
#号数越打盘子越大,三个座从左到右依次是A,B,C
#————————————汉诺塔程序定义部分————————
def run(n,A,B,C):
    if n==1:
        print("将%c座中%d盘子移动到%c座上"%(A,n,C))
    else:
        run(n-1,A,C,B)
        print("将%c座中%d盘子移动到%c座上"%(A,n,C))
        run(n-1,B,A,C)
#————————主程序部分————————
n=int(input("请输入你要移动的层数:"))
run(n,"A","B","C")
