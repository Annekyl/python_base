n=0
for j in range(len(ls)):
    #j的开始是0，所以下面的数为j+1
    for i in range(len(ls)-(j+1)):
        if(ls[j]>ls[i+j+1]):
                ls[j],ls[i+j+1]=ls[i+j+1],ls[j]
                n+=1
print(f"由普通选择排序法得出的结果为{ls}")
print(f"由普通选着排序法得出的交换次数为{n}")
