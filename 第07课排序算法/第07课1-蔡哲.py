ls = [12,40,10,9,8,-1,21,-6]
n=0
for j in range(len(ls)):
    f=j
    for i in range(len(ls)-(j+1)):
        if(ls[f]>ls[i+(j+1)]):
            f=i+(j+1)
    if(f!=j):
        ls[j],ls[f]=ls[f],ls[j]
        n+=1
print(f"由最优型选择排序法得出的结果为{ls}")
