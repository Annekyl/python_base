import turtle
turtle.pensize(5)
turtle.speed(3)


while(1):
    turtle.hideturtle
    turtle.penup()
    turtle.goto(-200,0)
    turtle.pendown()
    turtle.forward(50)
    turtle.left(70)
    turtle.forward(60)
    turtle.right(140)
    turtle.forward(150)
    turtle.left(140)
    turtle.forward(200)
    turtle.right(140)
    turtle.forward(160)
    turtle.left(140)
    turtle.forward(60)
    turtle.right(70)
    turtle.forward(50)
    turtle.hideturtle
    turtle.clear()
    turtle.penup()
    turtle.goto(-200,0)
    
